    function addToCart(name) {
        var uid = localStorage.getItem('INC');
        var usertype = localStorage.getItem('USERTYPEmlm');
        $.ajax({
            method: "POST",
            dataType: 'json',
            url: "functions.php",
            data:{fid:4,name:name,uid:uid,usertype:usertype}
        })
            .done(function(msg){
                if (msg.res == 1){
                    Swal.fire({
                        icon: 'warning',
                        title: 'This Product is already in your cart!',
                        showConfirmButton: false,
                        timer: 3000
                    });
                }
                else if (msg.res == 2){
                    Swal.fire({
                        icon: 'success',
                        title: 'Product Added to Cart!',
                        showConfirmButton: false,
                        timer: 3000
                    });
                    $('#cartCount').html(msg.item);
                }
            });
    }
